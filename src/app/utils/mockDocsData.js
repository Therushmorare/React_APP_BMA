// Helper function to filter documents by category
export const filterDocumentsByCategory = (documents, category) => {
  if (category === 'all') return documents;
  return documents.filter(doc => doc.category === category);
};

// Helper function to search documents
export const searchDocuments = (documents, query) => {
  if (!query) return documents;
  
  const lowercaseQuery = query.toLowerCase();
  return documents.filter(doc => 
    doc.name.toLowerCase().includes(lowercaseQuery) ||
    doc.category.toLowerCase().includes(lowercaseQuery) ||
    doc.type.toLowerCase().includes(lowercaseQuery) ||
    doc.uploaded_by.toLowerCase().includes(lowercaseQuery)
  );
};

// Helper function to paginate documents
export const paginateDocuments = (documents, page, pageSize) => {
  const startIndex = (page - 1) * pageSize;
  const endIndex = startIndex + pageSize;
  return {
    documents: documents.slice(startIndex, endIndex),
    total: documents.length,
    page,
    pageSize,
    totalPages: Math.ceil(documents.length / pageSize)
  };
};

